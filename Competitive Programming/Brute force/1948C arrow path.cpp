bool isPrime_sqrt(ll n)
{
    if (n == 1)
        return false;
    for (ll i = 2; i * i <= n; i++)
    {
        if (n % i == 0)
            return false;
    }
    return true;
}
long long binaryExponentiation(long long base, long long exponent, long long mod)
{
    long long res = 1;
    while (exponent > 0)
    {
        if (exponent % 2 == 1)
        {
            res = (res * base) % mod;
        }
        base = (base * base) % mod;
        exponent >>= 1;
    }
    return res;
}
long long getModularMultiplicativeInverse(long long num, long long mod)
{
    // multiplicative inverse only exists if num and mod are relatively prime (coprime) i.e gcd(num, mod) = 1
    if (__gcd(num, mod) != 1)
    {
        return -1;
    }
    return binaryExponentiation(num, mod - 2, mod);
}

/*
There is a grid, consisting of 2 rows and n columns. The rows are numbered from 1 to 2 from top to bottom. The columns are numbered from 1 to n

from left to right. Each cell of the grid contains an arrow pointing either to the left or to the right. No arrow points outside the grid.

There is a robot that starts in a cell (1,1)

. Every second, the following two actions happen one after another:

    Firstly, the robot moves left, right, down or up (it can't try to go outside the grid, and can't skip a move);
    then it moves along the arrow that is placed in the current cell (the cell it ends up after its move).

Your task is to determine whether the robot can reach the cell (2,n)
.
 */

inline bool inBounds(ll r, ll c, ll n, ll m)
{
    return r >= 0 && r < n && c >= 0 && c < m;
}
void solve()
{
    int n;
    cin >> n;
    vector<vector<char>> grid(2, vector<char>(n));

    for (int i = 0; i < 2; i++)
    {
        for (int j = 0; j < n; j++)
        {
            cin >> grid[i][j];
        }
    }

    queue<pii> q;
    q.emplace(0, 0);
    //% Simple BFS traversal
    vector<vector<bool>> visited(2, vector<bool>(n, false));
    visited[0][0] = true;

    while (!q.empty())
    {
        auto [r, c] = q.front();
        q.pop();

        if (r == 1 && c == n - 1)
        {
            cout << "YES" << endl;
            return;
        }
        for (auto [dr, dc] : dirs4)
        {
            int nr = r + dr, nc = c + dc;
            if (inBounds(nr, nc, 2, n))
            {
                //& According to question '<' means allowed dirs are col - 1 row + 1 or row - 1 and col + 1 row + 1 row - 1 for '>'
                if (grid[nr][nc] == '<')
                {
                    nc--;
                }
                else
                {
                    nc++;
                }

                //& We need to check visited array for the next cell where the robot will land instead of current one thats why nc-- and nc++
                if (visited[nr][nc])
                    continue;
                q.emplace(nr, nc);
                visited[nr][nc] = true;
            }
        }
    }

    cout << "NO" << endl;
}

