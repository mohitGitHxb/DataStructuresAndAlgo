bool isPrime_sqrt(ll n)
{
    if (n == 1)
        return false;
    for (ll i = 2; i * i <= n; i++)
    {
        if (n % i == 0)
            return false;
    }
    return true;
}
long long binaryExponentiation(long long base, long long exponent, long long mod)
{
    long long res = 1;
    while (exponent > 0)
    {
        if (exponent % 2 == 1)
        {
            res = (res * base) % mod;
        }
        base = (base * base) % mod;
        exponent >>= 1;
    }
    return res;
}
long long getModularMultiplicativeInverse(long long num, long long mod)
{
    // multiplicative inverse only exists if num and mod are relatively prime (coprime) i.e gcd(num, mod) = 1
    if (__gcd(num, mod) != 1)
    {
        return -1;
    }
    return binaryExponentiation(num, mod - 2, mod);
}

/*
You have 5 different types of coins, each with a value equal to one of the first 5 triangular numbers: 1, 3, 6, 10, and 15. These coin types are available in abundance. Your goal is to find the minimum number of these coins required such that their total value sums up to exactly n.

We can show that the answer always exists.
 */
void solve()
{
    /*
    & precomputed answers till N = 15, let's say our greedy approach fails for a number x.(for eg: x = 20,23,etc)

    &- Now for each x + 15, x + 30, x + 45... x + k.15 the greedy approach would fail here as well.

    &- If we take the modulo with 15 the possible remainders are in range [0,14] however when remainder is 5,8 (x = 20, x = 23) the approach fails hence we take care of these cases explicitly

    &- For any other case simply return n/15 + whatever the coins requied to cover up remainder(precomputed already and also remainder != [5,8])
     */
    vector<int> f{0, 1, 2, 1, 2, 3, 1, 2, 3, 2, 1, 2, 2, 2, 3};
    ll n;
    cin >> n;
    if (n < 15)
        cout << f[n % 15] << "\n";
    else if (n % 15 == 5)
        cout << n / 15 + 1 << "\n";
    else if (n % 15 == 8)
        cout << n / 15 + 2 << "\n";
    else
        cout << n / 15 + f[n % 15] << "\n";
}

