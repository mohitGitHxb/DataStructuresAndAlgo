bool isPowerOfTwo(unsigned long long n)
{
    return (n && !(n & (n - 1)));
}
void solve()
{
    unsigned long long n;
    cin >> n;
    //% edge case in case a number is a power of 2 it can't be represented as ai | ai+1
    if (isPowerOfTwo(n))
    {
        cout << 1 << "\n";
        cout << n << "\n";
        return;
    }
    bitset<64> prev(n);
    bitset<64> curr;
    vector<unsigned long long> ans;
    ans.pb(n);
    for (int i = 0; i < 64; i++)
    {
        /*
        & find the first set bit
         & then clear (unset) all set bits till 0th index

         */
        if (prev[i] == 1)
        {
            curr = prev;
            rforn(j, i)
            {
                curr[j] = 0;
            }
            unsigned ll prevNum = prev.to_ullong();
            unsigned ll currNum = curr.to_ullong();
            //& difference of prev and curr has to be positive as we are building decreasing sequence
            if (prevNum > currNum)
            {
                // deb(curr.to_ullong());
                // deb(prevNum);
                //~ pure observation: finding difference and OR it with curr gives the actual curr required to make curr | prev == given number(n)
                bitset<64> temp(n - prevNum);
                unsigned ll diff = temp.to_ullong();
                // deb(diff);
                // deb(((curr | temp) | prev).to_ullong());
                ans.pb((curr | temp).to_ullong());

                prev = curr;
            }
        }
    }
    reverse(all(ans));
    cout << ans.size() << "\n";
    forv(it, ans)
    {
        cout << it << " ";
    }
    cout << "\n";
}

