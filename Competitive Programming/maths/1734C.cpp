bool isPrime_sqrt(ll n)
{
    if (n == 1)
        return false;
    for (ll i = 2; i * i <= n; i++)
    {
        if (n % i == 0)
            return false;
    }
    return true;
}
long long binaryExponentiation(long long base, long long exponent, long long mod)
{
    long long res = 1;
    while (exponent > 0)
    {
        if (exponent % 2 == 1)
        {
            res = (res * base) % mod;
        }
        base = (base * base) % mod;
        exponent >>= 1;
    }
    return res;
}
long long getModularMultiplicativeInverse(long long num, long long mod)
{
    // multiplicative inverse only exists if num and mod are relatively prime (coprime) i.e gcd(num, mod) = 1
    if (__gcd(num, mod) != 1)
    {
        return -1;
    }
    return binaryExponentiation(num, mod - 2, mod);
}

/*
One operation should be used to remove every element not belonging to T

.

Let v
be an element not belonging to T. Suppose a x-cost operation removes value v, then v must be divisible by x. Furthermore, the multiples x,2x,⋯(k−1)x must have been already removed from S, where we write v=kx

.

Since removed elements stay removed, the above is only possible if all of x,2x,⋯(k−1)x
does not belong to T

.

For each v
, let f(v) be the smallest integer x satisfying the above condition. As we can always remove v using a v-cost operation, f(v)≤v and in particular f(v)

exists.

The total cost must be at least ∑i∉Tf(i)

. We claim that this cost can be achieved.

To do so, we should remove the required elements in ascending order. When removing v
, we assume all w∉T with w<v have already been removed. At this state, an f(v)-cost operation would be able to remove v

.

It remains to find the values f(v)
. To do so efficiently, we can perform the above process in a bottom-up manner similar to the Sieve of Eratosthenes. Please refer to the code below for implementation details. The overall complexity is n(1+12+13+⋯+1n)=Θ(nlogn).
 */

void solve()
{
    int n;
    cin >> n;
    bool a[n + 1];
    string str;
    cin >> str;
    for (int i = 1; i <= n; i++)
    {
        a[i] = (str[i - 1] == '1');
    }
    long long ans = 0;
    int cost[n + 1];
    for (int i = n; i >= 1; i--)
    {
        for (int j = i; j <= n; j += i)
        {
            if (a[j])
                break;
            cost[j] = i;
        }
    }
    for (int i = 1; i <= n; i++)
    {
        if (!a[i])
            ans += cost[i];
    }
    cout << ans << '\n';
}

