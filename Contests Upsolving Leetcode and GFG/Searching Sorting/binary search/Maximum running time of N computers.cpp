bool isPrime_sqrt(ll n)
{
    if (n == 1)
        return false;
    for (ll i = 2; i * i <= n; i++)
    {
        if (n % i == 0)
            return false;
    }
    return true;
}
long long binaryExponentiation(long long base, long long exponent, long long mod)
{
    long long res = 1;
    while (exponent > 0)
    {
        if (exponent % 2 == 1)
        {
            res = (res * base) % mod;
        }
        base = (base * base) % mod;
        exponent >>= 1;
    }
    return res;
}
long long getModularMultiplicativeInverse(long long num, long long mod)
{
    // multiplicative inverse only exists if num and mod are relatively prime (coprime) i.e gcd(num, mod) = 1
    if (__gcd(num, mod) != 1)
    {
        return -1;
    }
    return binaryExponentiation(num, mod - 2, mod);
}

class Solution
{
private:
    /*
You have n computers. You are given the integer n and a 0-indexed integer array batteries where the ith battery can run a computer for batteries[i] minutes. You are interested in running all n computers simultaneously using the given batteries.

Initially, you can insert at most one battery into each computer. After that and at any integer time moment, you can remove a battery from a computer and insert another battery any number of times. The inserted battery can be a totally new battery or a battery from another computer. You may assume that the removing and inserting processes take no time.

Note that the batteries cannot be recharged.

Return the maximum number of minutes you can run all the n computers simultaneously.



Example 1:

Input: n = 2, batteries = [3,3,3]
Output: 4
Explanation:
Initially, insert battery 0 into the first computer and battery 1 into the second computer.
After two minutes, remove battery 1 from the second computer and insert battery 2 instead. Note that battery 1 can still run for one minute.
At the end of the third minute, battery 0 is drained, and you need to remove it from the first computer and insert battery 1 instead.
By the end of the fourth minute, battery 1 is also drained, and the first computer is no longer running.
We can run the two computers simultaneously for at most 4 minutes, so we return 4.

Example 2:

Input: n = 2, batteries = [1,1,1,1]
Output: 2
Explanation:
Initially, insert battery 0 into the first computer and battery 2 into the second computer.
After one minute, battery 0 and battery 2 are drained so you need to remove them and insert battery 1 into the first computer and battery 3 into the second computer.
After another minute, battery 1 and battery 3 are also drained so the first and second computers are no longer running.
We can run the two computers simultaneously for at most 2 minutes, so we return 2.

    @ Approach
    ! Wrong approach: trying to get the sum of capacity of all the batteries
    ! and then checking for condition whether we can run the computers using this total capacity
    ! i.e totalCapacity >= number of computers * minutes of running

    ! The reason why this fails is because we need to check whether battery[i] can run the computer for atleast mid minutes or not

    * Correct approach:
    If we observe carefully, we will realize that the maximum time for which we can run computers is monotonic in nature. This is because, if we can run n computers for t minutes, we can definitely run n computers for t - 1 minutes. Therefore, this gives us a clue that binary search might be applicable in this problem.
Approach

We can set our lo and hi pointers as minimum of all the batteries and the sum of all the given batteries respectively.

The main logic which verifies if a given time limit is valid is as following:

Let's assume our current time limit is mid. We must therefore run each computer for mid minutes. How much battery is this going consume? To answer this, let's assume that there is only one computer which consumes battery power equivalent to n computers.

Orginally, each computer consumed one unit of battery power per unit time. If we stack this rate of consumption (for n computers) onto one computer, it is going to consume n units of battery power per unit time! In order to last mid minutes, our batteries must supply at least midâˆ—n units of power!
     */
public:
    const int mod = 1e9 + 7;
    Solution()
    {
        fast_read();
    }
    bool canRun(ll mid, vector<int> &batteries, int n)
    {
        ll requiredRunningTime = mid * n;
        forv(battery, batteries)
        {
            requiredRunningTime -= min(battery * 1LL, mid);
            if (requiredRunningTime <= 0)
                return true;
        }
        return requiredRunningTime <= 0;
    }
    long long maxRunTime(int n, vector<int> &batteries)
    {
        ll low = MIN(batteries), high = SUM(batteries) / n;
        ll ans = 0;
        while (low <= high)
        {
            ll mid = low + (high - low) / 2;
            if (canRun(mid, batteries, n))
            {
                ans = max(ans, mid);
                low = mid + 1;
            }
            else
            {
                high = mid - 1;
            }
        }
        return ans;
    }
};
