bool isPrime_sqrt(ll n)
{
    if (n == 1)
        return false;
    for (ll i = 2; i * i <= n; i++)
    {
        if (n % i == 0)
            return false;
    }
    return true;
}
long long binaryExponentiation(long long base, long long exponent, long long mod)
{
    long long res = 1;
    while (exponent > 0)
    {
        if (exponent % 2 == 1)
        {
            res = (res * base) % mod;
        }
        base = (base * base) % mod;
        exponent >>= 1;
    }
    return res;
}
long long getModularMultiplicativeInverse(long long num, long long mod)
{
    // multiplicative inverse only exists if num and mod are relatively prime (coprime) i.e gcd(num, mod) = 1
    if (__gcd(num, mod) != 1)
    {
        return -1;
    }
    return binaryExponentiation(num, mod - 2, mod);
}

// class for Disjoint Set Union
class DSU
{
public:
    vector<int> parent, size;
    DSU(int n)
    {
        parent.resize(n + 1);
        size.resize(n + 1, 1);
        iota(parent.begin(), parent.end(), 0);
    }

    int find(int x)
    {
        if (parent[x] == x)
            return x;
        return parent[x] = find(parent[x]);
    }
    void unionSet(int x, int y)
    {
        int xRoot = find(x);
        int yRoot = find(y);
        if (xRoot == yRoot)
            return;
        if (size[xRoot] < size[yRoot])
            swap(xRoot, yRoot);
        parent[yRoot] = xRoot;
        size[xRoot] += size[yRoot];
    }
    int getAllDisconnectedComponents()
    {
        int ans = 0;
        for (int i = 1; i <= parent.size(); i++)
        {
            if (parent[i] == i)
                ans++;
        }
        return ans;
    }
};

class Solution
{
private:
    /*
There is a school that has classes of students and each class will be having a final exam. You are given a 2D integer array classes, where classes[i] = [passi, totali]. You know beforehand that in the ith class, there are totali total students, but only passi number of students will pass the exam.

You are also given an integer extraStudents. There are another extraStudents brilliant students that are guaranteed to pass the exam of any class they are assigned to. You want to assign each of the extraStudents students to a class in a way that maximizes the average pass ratio across all the classes.

The pass ratio of a class is equal to the number of students of the class that will pass the exam divided by the total number of students of the class. The average pass ratio is the sum of pass ratios of all the classes divided by the number of the classes.

Return the maximum possible average pass ratio after assigning the extraStudents students. Answers within 10-5 of the actual answer will be accepted.



Example 1:

Input: classes = [[1,2],[3,5],[2,2]], extraStudents = 2
Output: 0.78333
Explanation: You can assign the two extra students to the first class. The average pass ratio will be equal to (3/4 + 3/5 + 2/2) / 3 = 0.78333.

Example 2:

Input: classes = [[2,4],[3,9],[4,5],[2,10]], extraStudents = 4
Output: 0.53485


     */
public:
    double maxAverageRatio(vector<vector<int>> &classes, int extraStudents)
    {
        // Function to calculate the gain in pass ratio
        auto gain = [](int pass, int total) -> double
        {
            return (double)(pass + 1) / (total + 1) - (double)pass / total;
        };

        // Priority queue to store classes with the highest potential gain
        priority_queue<pair<double, pair<int, int>>> pq;
        for (auto &cls : classes)
        {
            pq.push({gain(cls[0], cls[1]), {cls[0], cls[1]}});
        }

        // Distribute extra students
        while (extraStudents-- > 0)
        {
            auto [maxGain, cls] = pq.top();
            pq.pop();
            int pass = cls.first, total = cls.second;
            pq.push({gain(pass + 1, total + 1), {pass + 1, total + 1}});
        }

        // Calculate the final average pass ratio
        double avg = 0.0;
        while (!pq.empty())
        {
            auto [_, cls] = pq.top();
            pq.pop();
            avg += (double)cls.first / cls.second;
        }

        return avg / classes.size();
    }
};

