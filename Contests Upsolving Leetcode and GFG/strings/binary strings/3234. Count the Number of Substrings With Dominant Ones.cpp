bool isPrime_sqrt(ll n)
{
    if (n == 1)
        return false;
    for (ll i = 2; i * i <= n; i++)
    {
        if (n % i == 0)
            return false;
    }
    return true;
}
long long binaryExponentiation(long long base, long long exponent, long long mod)
{
    long long res = 1;
    while (exponent > 0)
    {
        if (exponent % 2 == 1)
        {
            res = (res * base) % mod;
        }
        base = (base * base) % mod;
        exponent >>= 1;
    }
    return res;
}
long long getModularMultiplicativeInverse(long long num, long long mod)
{
    // multiplicative inverse only exists if num and mod are relatively prime (coprime) i.e gcd(num, mod) = 1
    if (__gcd(num, mod) != 1)
    {
        return -1;
    }
    return binaryExponentiation(num, mod - 2, mod);
}

class Solution
{
private:
    /*
You are given a binary string s.

Return the number of
substrings
with dominant ones.

A string has dominant ones if the number of ones in the string is greater than or equal to the square of the number of zeros in the string.



Example 1:

Input: s = "00011"

Output: 5

Explanation:

The substrings with dominant ones are shown in the table below.
i	j	s[i..j]	Number of Zeros	Number of Ones
3	3	1	0	1
4	4	1	0	1
2	3	01	1	1
3	4	11	0	2
2	4	011	1	2

Example 2:

Input: s = "101101"

Output: 16

Explanation:

The substrings with non-dominant ones are shown in the table below.

Since there are 21 substrings total and 5 of them have non-dominant ones, it follows that there are 16 substrings with dominant ones.
i	j	s[i..j]	Number of Zeros	Number of Ones
1	1	0	1	0
4	4	0	1	0
1	4	0110	2	2
0	4	10110	2	3
1	5	01101	2	3


Constraints:
    @ Approach :
    ! This question is not MEDIUM at all it has been rated 2200+
    * There are certain set of patterns which follow a similar approach just like this q
    * count subarrays with:
    * number of zeros == number of ones (take zeros as -1 and prefix sum + hashing)
    * number of zeros * q == number of ones * p (take zeros as -q and ones as p then find the count of subarrays having sum 0 using similar technique like prefix sum + hashing)
    * number of zeros > number of ones
    * number fo zeros > numbers of ones ^ k
    ^ number of zeros = numbers of ones ^ k
    & can solve this by enumerating all possible values of ones because
    & the subarray length would be X^k + X <= total length of array
    % hence our X is <= n^1/k at max

    ~ TIP: whenever we are enumerating on one thing always do it on more restrictive thing
     */
public:
    const int mod = 1e9 + 7;
    Solution()
    {
        fast_read();
    }
    int numberOfSubstrings(string s)
    {
        int ans = 0;
        int n = s.length();
        vector<int> prefix(n, 0);
        for (int i = 0; i < n; i++)
        {
            if (s[i] == '1')
            {
                prefix[i] = 1;
            }
        }
        for (int i = 1; i < n; i++)
        {
            prefix[i] += prefix[i - 1];
        }
        for (int i = 0; i < n; i++)
        {
            int zeros = 0, ones = 0;
            for (int j = i; j < s.length(); j++)
            {
                ones = prefix[j];
                if (i != 0)
                    ones -= prefix[i - 1];
                zeros = j - i + 1 - ones;

                // see if condition is false you will try to make it true as soon as possile you will
                // not want unusual iterations that's why we are incrementing j upto the desired mark
                // which will decrease our time complexity
                if (zeros * zeros > ones)
                    j += ((zeros * zeros) - ones - 1);

                // if condition true
                if (zeros * zeros <= ones)
                {
                    // we got one of the desired substring increment ans
                    ans++;

                    // check for other substring starting from i
                    // this is working like if we have more number of ones than without iterating
                    // we will increment here itself to avoid again unusual iterations
                    if (zeros * zeros < ones)
                    {
                        int diff = sqrt(ones) - (zeros);
                        int nextj = j + diff;
                        // if out of bound please take whole upto last into consideration
                        if (nextj >= n)
                        {
                            ans += (n - j - 1);
                        }
                        // else add upto the mark which is feasible
                        else
                        {
                            ans += diff;
                        }
                        // this will now help to reduce the number of iteration
                        j = nextj;
                    }
                }
            }
        }
        return ans;
    }
};
